package ru.javarush.level4;

public class Person {
    String name;
    int age;
    String address;
    char sex;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAddress() {
        return address;
    }

    public void  setSex() {
        this.sex = sex;
    }

    public char getSex() {
        return sex;
    }
}
